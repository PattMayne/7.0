    <p>
        This is what I want from a puerh tea. The musk is muted and mellow, you can taste the fermentation with a floral backdrop. It's richly evened out, flavorful as broth but not overpowering. And with 17 years of aging the tea itself is not bitter. There's nothing sharp about this. It's a beautiful and delicious tea. Not much hint of mushroom or forest floor. I mean, they're there, but the floral sweetness is more prominent. This might be my favorite tea. It certainly rivals the 2004 vintage I used to get (my vendor ran out).
    </p>

    <p>
        It's easy to taste the difference between the regular 2008 batch and this "reserve" batch. Robustly organic, yet elgant and delicate. This is my new king.
